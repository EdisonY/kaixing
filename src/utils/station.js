/**
* 以对象形式获取车站
* @param {*} currentLine 
*/
function getStationsObj(currentLine) {
let station = stations.find(s => {
    return s.name == currentLine;
});
let result = {};
let index = 1;
if (station) {
    station.data.forEach(item => {
        result[item[2]] = index++;
    });
}
return result;
}

